
def countNegatives(grid):
    c = 0
    for r in grid:
        if len(r) and r[0] < 0:
            c+=len(r)
        elif len(r) and r[-1] < 0: 
            for i, j in zip(range(len(r)),range(len(r)-1, -1, -1)): 
                if r[i] < 0:
                    c += len(r) - i
                    break
                elif r[j] >= 0:
                    c += len(r) - (j + 1)
                    break
                if i > j: break
    return c

